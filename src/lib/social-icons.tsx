/** Зарегистрированные ключи → SVG. Неизвестный ключ на странице обрабатывается в {@link SocialIcon}. */
export const ICONS = {
  instagram: IconInstagram,
  youtube: IconYoutube,
  facebook: IconFacebook,
  tiktok: IconTiktok,
  telegram: IconTelegram,
  whatsapp: IconWhatsapp,
  link: IconLink,
} as const

export type SocialIconKey = keyof typeof ICONS

